export class File {
  /**
   * Date the file was created
   */
  created: string;

  /**
   * The data of the file
   */
  data: any[];

  /**
   * Id of the file
   */
  id: number;

  /**
   * Filename
   */
  name: string;

  /**
   * Date the file was changed
   */
  updated: string;

  /**
   * Resource_uri
   */
  uri: string;
}
